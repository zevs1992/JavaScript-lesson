// добавить => открывается втрой промпт и вводим значения
// удалить через пробел индекс элемента
// после каждого действия отображается в косоль лог



let data = []


const checkAction= (act) => {
    if(act.toLowerCase() === 'добавить'){
        return 1
    } else if(act.toLowerCase() === 'удалить'){
        return 0
    }
}

const actionReduce = (act) => {
    let newAction = prompt('продолжай')
    if(act === 1){
        addData(newAction)
    } else {
        delData(newAction)
    }
}

const addData = (str) => {
    data.push(str)
}

const delData = (num) => {
    data.splice(num-1, 1)
}


while(true){
    let action = prompt('введите действие')
    actionReduce(checkAction(action))
    console.log(data)
}